export function t(key, values = {}) {
  const template = key.split(".").reduce((current, part) => current?.[part], messages);
  const text = typeof template === "string" ? template : key;
  return text.replace(/\{([A-Za-z0-9_]+)\}/g, (_, name) => String(values[name] ?? ""));
}
